function hitungLuasPersegiPanjang(panjang,lebar){
    luaspesegipanjang = panjang * lebar;
    document.write('Nilai Luas Persegi Panjang : ' + luaspesegipanjang);
}
var panjang = prompt('Masukan Nilai Panjangnya');
var lebar = prompt('Masukan Nilai Lebarnya');
hitungLuasPersegiPanjang(panjang,lebar);